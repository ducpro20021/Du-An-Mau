    private String LayMaCD(String ten) {
        List<ChuyenDe> ls = cddao.selectAll();
        String ma = null;
        for (int i = 0; i < ls.size(); i++) {
            if (ten.equals(ls.get(i).getTenCD())) {
                ma = ls.get(i).getMaCD();
            }
        }
        return ma;
    }
