    private void FillTable() {
        DefaultTableModel mol = (DefaultTableModel) tblKhoaHoc.getModel();

        mol.setRowCount(0);
        try {
            String ten = String.valueOf(cboChuyenDe.getSelectedItem());
            String ma = LayMaCD(ten);
            List<KhoaHoc> list = khdao.selectByChuyenDe(ma);

            for (KhoaHoc kh : list) {
                mol.addRow(new Object[]{kh.getMaKH(), kh.getThoiLuong(), kh.getHocPhi(), dateHelper.toString(kh.getNgayKG()),  dateHelper.toString(kh.getNgayTao())});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void TimKiemInDanhSach(int ma) {
        KhoaHoc kh = khdao.selectById(ma);
        String ten = String.valueOf(cboChuyenDe.getSelectedItem());
        String macd = LayMaCD(ten);
        List<KhoaHoc> list = khdao.selectByChuyenDe(macd);
        int index = -1;
        if (kh == null) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy.");
            txtSearch.requestFocus();
            return;
        } else {
            for (int i = 0; i < list.size(); i++) {
                if (kh.getMaKH() == list.get(i).getMaKH()) {
                    index = i;
                    break;
                } else {
                    index = -1;
                }
            }
        }
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "Khóa học này không có trong chuyên đề " + ten);
            txtSearch.requestFocus();
            return;
        } else {
            this.tblKhoaHoc.setRowSelectionInterval(index, index);
        }
    }

