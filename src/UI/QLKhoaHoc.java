    public KhoaHoc GetForm() {
        KhoaHoc kh = new KhoaHoc();
        List<KhoaHoc> ls = khdao.selectAll();
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
            String ngaytao = txtNgayTao.getText();
            String ngayKG = txtNgaKG.getText();
            String ten = (String) cboChuyenDe.getSelectedItem();
            String ma = LayMaCD(ten);
            kh.setMaKH(ls.size() + 1);
            kh.setMaCD(ma);
            kh.setMaNV(txtNguoiTao.getText());
            kh.setHocPhi(Integer.valueOf(txtHocPhi.getText()));
            kh.setThoiLuong(Integer.valueOf(txtThoiLuong.getText()));
            kh.setNgayKG(sdf.parse(ngayKG));
            kh.setNgayTao(sdf.parse(ngaytao));
            kh.setGhiChu(txtGhiChu.getText());
            return kh;
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }

